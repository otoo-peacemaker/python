# Example of a single-line string
str1 = "Hello, world!"
print(str1)  # Output: Hello, world!


# Example of a multi-line string
str2 = """This is a multi-line
string in Python."""
print(str2)
# Output:
# This is a multi-line
# string in Python.


# Example of string concatenation
str3 = "Hello" + " " + "world!"
print(str3)  # Output: Hello world!


# Example of string interpolation
name = "John"
age = 30
str4 = f"My name is {name} and I am {age} years old."
print(str4)  # Output: My name is John  I am 30 years old.
