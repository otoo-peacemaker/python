# Composite data types in Python are data structures that can hold multiple elements of different data types.
# These data types allow you to organize and manipulate complex data efficiently.
# Examples of composite data types in Python include lists, tuples, dictionaries, and sets.
# Here's a brief overview of each:

# [1]. Lists:
#    - Lists are ordered collections of items.
#    - Elements in a list can be of different data types.
#    - Lists are mutable, meaning you can modify the elements after creation.
#    - Example:
my_list = [1, 2, "hello", True]
# [2]. Tuples:
#    - Tuples are ordered collections of items, similar to lists.
#    - Elements in a tuple can be of different data types.
#    - Tuples are immutable, meaning you cannot modify the elements after creation.
#    - Example:
my_tuple = (1, 2, "hello", True)
# [3]. Dictionaries:
#    - Dictionaries are unordered collections of key-value pairs.
#    - Each element in a dictionary consists of a key and its corresponding value.
#    - Keys must be unique and immutable (e.g., strings, numbers, tuples).
#    - Values can be of any data type.
#    - Example:

my_dict = {'name': 'John', 'age': 30, 'city': 'New York'}

# [4]. Sets:
#    - Sets are unordered collections of unique elements.
#    - Sets do not allow duplicate elements.
#    - Sets are mutable, meaning you can add or remove elements after creation.
#    - Example:
my_set = {1, 2, 3, 4, 5}